package by.epam.cdp.java.nypresent.validation;

public class PresentStorageException extends Exception {

	private static final long serialVersionUID = 3786701223920765463L;

	public PresentStorageException() { 
		
		 super();
		 
		 }
	
	  public PresentStorageException(String message) { 
		  
		  super(message); 
		  
		  }
	  
	  public PresentStorageException(String message, Throwable cause) { 
		  
		  super(message, cause);
		  
		  }
	  
	  public PresentStorageException(Throwable cause) {
		  
		  super(cause);
		  
		  }
	  

}
